public class Dog {

    String name;
    int age;
    String breed;
    int legs;
    String color;


    public String toString() {
        String output = "Name: " + name;
        output += "\nAge: " + age;
        output += "\nGrade: " + breed;
        output += "\nLegs : " + legs;
        output += "\nColor : " + color;

        return output;
    }

}
